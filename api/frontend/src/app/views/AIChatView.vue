<template>
  <div class="flex flex-col h-full mt-4">
    <div class="flex-1 overflow-y-auto p-4 space-y-4 bg-base-200 rounded-xl">
      <div class="chat chat-start">
        <div class="chat-bubble chat-bubble-primary">
          Hello! I am your AI assistant running on the Go-Full framework.
        </div>
      </div>
      <div v-for="(msg, index) in messages" :key="index" :class="msg.role === 'user' ? 'chat chat-end' : 'chat chat-start'">
        <div :class="msg.role === 'user' ? 'chat-bubble chat-bubble-accent' : 'chat-bubble chat-bubble-primary'">
          {{ msg.content }}
        </div>
      </div>
    </div>
    
    <div class="mt-4 flex gap-2">
      <input 
        v-model="inputRaw" 
        @keyup.enter="sendMessage"
        type="text" 
        placeholder="Type a prompt for the AI PoC..." 
        class="input input-bordered w-full" />
      <button @click="sendMessage" class="btn btn-primary">Send</button>
    </div>
  </div>
</template>

<script setup lang="ts">
import { ref } from 'vue'

const inputRaw = ref('')
const messages = ref<{role: string, content: string}[]>([])

const sendMessage = () => {
  if (!inputRaw.value.trim()) return
  // Add user message to UI
  messages.value.push({ role: 'user', content: inputRaw.value })
  
  // TODO: Wire this to the backend API generated by oapi-codegen
  // Example: api.chat({ prompt: inputRaw.value })
  
  // Simulate AI Response
  setTimeout(() => {
    messages.value.push({ role: 'assistant', content: 'This is a simulated response. Once the API is mapped via OpenAPI, the response will stream here.' })
  }, 1000)
  
  inputRaw.value = ''
}
</script>
